var findLadders = function (beginWord, endWord, wordList) {
  let ans = [];
  let reverse = new Map();
  let wordSet = new Set(wordList);
  wordSet.delete(beginWord);
  let queue = [beginWord];
  let nextLevel = new Set();
  let findEnd = false;

  while (queue.length) {
    let word = queue.shift();
    for (let next of wordSet) {
      if (isLadder(word, next)) {
        let reverseLadders = reverse.get(next) || new Set();
        reverseLadders.add(word);
        reverse.set(next, reverseLadders);

        if (endWord === next) {
          findEnd = true;
        }
        nextLevel.add(next);
      }
    }
    if (!queue.length) {
      if (findEnd) break;
      queue.push(...nextLevel);
      for (let word of nextLevel) {
        wordSet.delete(word);
      }
      nextLevel.clear();
    }
  }
  if (!findEnd) return ans;
  let path = new Set([endWord]);
  findPath(endWord, beginWord, reverse, ans, path);
  return ans;
};

function findPath(endWord, beginWord, graph, ans, path) {
  let next = graph.get(endWord);
  if (next === undefined) return;
  for (let word of next) {
    path.add(word);
    if (beginWord === word) {
      let shortestPath = Array.from(path);
      shortestPath.reverse();
      ans.push(shortestPath);
    } else {
      findPath(word, beginWord, graph, ans, path);
    }
    path.delete(word);
  }
}

function isLadder(s, t) {
  if (s.length !== t.length) return false;
  let diffCount = 0;
  for (let i = 0; i < s.length; i++) {
    if (s[i] !== t[i]) diffCount++;
    if (diffCount > 1) return false;
  }
  return diffCount === 1;
}

